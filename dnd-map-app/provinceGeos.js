// https://stackoverflow.com/questions/14484787/wrap-text-in-javascript
function stringDivider(str, width, spaceReplacer) {
	if (str.length > width) {
		let p = width;
		while (p > 0 && str[p] != " " && str[p] != "-") {
			p--;
		}
		if (p > 0) {
			let left;
			if (str.substring(p, p + 1) == "-") {
				left = str.substring(0, p + 1);
			} else {
				left = str.substring(0, p);
			}
			const right = str.substring(p + 1);
			return left + spaceReplacer + stringDivider(right, width, spaceReplacer);
		}
	}
	return str;
}

const getText = function (feature, resolution) {
	const type = "wrap";
	const maxResolution = 1200;
	let text = feature.get("name");

	if (resolution > maxResolution) {
		text = "";
	} else if (type == "hide") {
		text = "";
	} else if (type == "shorten") {
		text = text.trunc(12);
	} else if (type == "wrap") {
		text = stringDivider(text, 16, "\n");
	}

	return text;
};

let openSansAdded = false;
const createTextStyle = function (feature, resolution) {
	const align = "";
	const baseline = "middle";
	const size = "18px";
	const height = 1.2;
	const offsetX = parseInt(0, 10);
	const offsetY = parseInt(0, 10);
	const weight = "Bold";
	const placement = "Point";
	const maxAngle = 45;
	// const overflow = dom.overflow ? dom.overflow.value == "true" : undefined;
	// const rotation = parseFloat(dom.rotation.value);
	if (!openSansAdded) {
		const openSans = document.createElement("link");
		openSans.href = "https://fonts.googleapis.com/css?family=Open+Sans";
		openSans.rel = "stylesheet";
		document.head.appendChild(openSans);
		openSansAdded = true;
	}
	const font = weight + " " + size + "/" + height + " " + "'Open Sans'";
	const fillColor = feature.get("color");
	const outlineColor = "#ffffff";
	const outlineWidth = 3;

	return new Text({
		textAlign: align == "" ? undefined : align,
		textBaseline: baseline,
		font: font,
		text: getText(feature, resolution),
		fill: new Fill({ color: fillColor }),
		stroke: new Stroke({ color: outlineColor, width: outlineWidth }),
		offsetX: offsetX,
		offsetY: offsetY,
		placement: placement,
		maxAngle: maxAngle,
	});
};

